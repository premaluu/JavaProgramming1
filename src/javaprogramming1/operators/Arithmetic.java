package javaprogramming1.operators;

public class Arithmetic {
    public static void main(String args[]) {
        int a = 10;
        int b = 15;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}