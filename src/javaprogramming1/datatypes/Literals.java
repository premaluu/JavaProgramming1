package javaprogramming1.datatypes;

public class Literals {
    public static void main(String args[]) {
        int a = 10;
        int b = 027;
        int c = 0x64;
        int d = 123_456_789;
        double e = 0x13.2P2;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}